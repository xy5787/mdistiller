"""
Synthetic dataset generated by ManifoldProbe pixel sweep.
Loads pre-computed (image, soft_label, hard_label) triples from a .pt file.
"""
import torch
from torch.utils.data import Dataset, DataLoader


class SyntheticSweepDataset(Dataset):
    """
    Dataset of synthetic images created by sweeping salient pixels.
    Each sample: (image [1,28,28], soft_label [10], hard_label scalar)
    """

    def __init__(self, sweep_path):
        data = torch.load(sweep_path, weights_only=False)
        self.images = data["images"]          # [N, 1, 28, 28]
        self.soft_labels = data["soft_labels"] # [N, 10]
        self.hard_labels = data["hard_labels"] # [N]

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        return self.images[idx], self.soft_labels[idx], self.hard_labels[idx]


def get_synthetic_dataloader(sweep_path, batch_size=128, num_workers=4):
    """Returns a DataLoader for the synthetic sweep dataset."""
    dataset = SyntheticSweepDataset(sweep_path)
    loader = DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True,
    )
    return loader, len(dataset)
